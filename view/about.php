            <p>Jean Forteroche passe la plus grande partie de son enfance en Bretagne, dès que son père, artiste peintre, devient conservateur des musées de Quimper. Il revient à Paris faire ses études supérieures en khâgne. Licencié de Lettres et de Philosophie, il choisit l'Agrégation de Lettres en 1969.</p>
            <p>Marié et père de deux enfants, il enseigne alors la Littérature pendant 15 ans dans un lycée de Nantes, tout en participant à des travaux de recherche sur la sémiologie de l'image et sur la lecture. Après un an de stage spécialisé sur l'image et le son à l'École Normale Supérieure de Saint-Cloud, il est chargé de la mise en place d'une structure de formation à l'audio-visuel pour les enseignants de l'Académie de Nantes.</p>
            <p>En 1987 il contribue à la création à Nantes de la première Classe Préparatoire aux Grandes Écoles de cinéma (FEMIS et E.N.S. Louis Lumière) dans laquelle il assure les cours de réalisation cinématographique et d'analyse filmique. </p>
            <p>Il y consacrera les 19 années suivantes sans cesser parallèlement d'écrire. Il réalise durant cette période de nombreux films documentaires et institutionnels avec ses étudiants, dont un moyen métrage sur la situation politique en Roumanie lors de la chute du régime de Ceausescu qui sera diffusé sur FR3.</p>
            <p>Professeur honoraire depuis 2006, il se voue désormais totalement à la littérature.</p>
